package day04_practice;

public class WarmUp_Concatenation {
    public static void main(String[] args) {

        String firstName= "Masha",
                lastName= "Kakasha",
                FullName= firstName + " " + lastName,
                from= "Colombia",
                favoriteMusic= "Bosanova",
                favoriteBook= "Coco";
        char gender= 'F';
        int age= 18;
        boolean isMarried= false;
        System.out.println();
        System.out.println("FullName = " + FullName); // + operator
        System.out.println("from = " + from);
        System.out.println("favoriteMusic = " + favoriteMusic);
        System.out.println("favoriteBook = " + favoriteBook);
        System.out.println("gender = " + gender);
        System.out.println("age = " + age);
        System.out.println("isMarried = " + isMarried);

    }
}
