package day14_ForLoop;

public class ForLoop2 {
    public static void main(String[] args) {

        for (int i =1; i<=10; i++){
            System.out.print("Cydeo ");
        }
        System.out.println();
        System.out.println("================");

        for (int i=10; i>=1; i--){
            System.out.print("Cydeo"+i+" ");
        }
        System.out.println();
        System.out.println("================");

    }
}
